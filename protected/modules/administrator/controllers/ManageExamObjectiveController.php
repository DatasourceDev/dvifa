<?php

class ManageExamObjectiveController extends AdministratorController {

    public $layout = '/manageExam/_layout';
    public $title = 'วัตถุประสงค์ในการสอบ';

    public function actionIndex() {
        $model = new CodeObjective('search');
        $model->unsetAttributes();
        $dataProvider = $model->search();

        $this->render('index', array(
            'model' => $model,
            'dataProvider' => $dataProvider,
        ));
    }

    public function actionCreate() {
        $model = new CodeObjective;
        $data = Yii::app()->request->getPost('CodeObjective');
        if (isset($data)) {
            $model->attributes = $data;
            if ($model->save()) {
                Yii::app()->user->setFlash('success', Helper::MSG_TH_SAVED);
                $this->redirect(array('index'));
            }
        }
        $this->render('form', array(
            'model' => $model,
        ));
    }

    public function actionUpdate($id) {
        $model = CodeObjective::model()->findByPk($id);
        $data = Yii::app()->request->getPost('CodeObjective');
        if (isset($data)) {
            $model->attributes = $data;
            if ($model->save()) {
                Yii::app()->user->setFlash('success', Helper::MSG_TH_SAVED);
                $this->redirect(array('index'));
            }
        }
        $this->render('form', array(
            'model' => $model,
        ));
    }

    public function actionDelete($id) {
        $model = CodeObjective::model()->findByPk($id);
        $model->delete();
        if (!Yii::app()->request->isAjaxRequest) {
            $this->redirect(array('index'));
        }
    }

}
